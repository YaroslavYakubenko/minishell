#include "minishell.h"

int	ft_check_quote(char *cmd)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (cmd[i])
	{
		if (cmd[i] == '\'' || cmd[i] == '\"')
			j = 1;
		i++;
	}
	return (j);
}
